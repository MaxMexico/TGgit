class addition:
    def __init__(self, a, b):
        self.a = a
        self.b = b

    def addition(self):
        return self.a + self.b
    

class soustraction:
    def __init__(self, a, b):
        self.a = a
        self.b = b

    def soustraction(self):
        return self.a - self.b

class division:
    def __init__(self, a, b):
        self.a = a
        self.b = b

    def division(self):
        return self.a / self.b
    


